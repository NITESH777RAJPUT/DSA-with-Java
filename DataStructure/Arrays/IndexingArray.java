package DataStructure.Arrays;

public class IndexingArray {
    public static void main(String[] args) {
        // Defining and initializing an array of strings
        String[] fruits = { "Apple", "Banana", "Cherry", "Date", "Elderberry" };

        // Accessing and printing each element of the array using indexing
        System.out.println(fruits[0]); // Output: Apple
        System.out.println(fruits[1]); // Output: Banana
        System.out.println(fruits[2]); // Output: Cherry
        System.out.println(fruits[3]); // Output: Date
        System.out.println(fruits[4]); // Output: Elderberry
    }
}
