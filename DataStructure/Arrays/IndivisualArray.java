package DataStructure.Arrays;

public class IndivisualArray {
    public static void main(String[] args) {
        // Defining and initializing an array of doubles
        int[] arr = new int[5];
        arr[0] = 5;
        arr[1] = 10;
        arr[2] = 15;
        arr[3] = 20;
        arr[4] = 25;
        // Accessing and printing each element of the array individually
        System.out.println(arr[0]); // Output: 5
        System.out.println(arr[1]); // Output: 10
        System.out.println(arr[2]); // Output: 15
        System.out.println(arr[3]); // Output: 20
        System.out.println(arr[4]); // Output: 25
    }
}
