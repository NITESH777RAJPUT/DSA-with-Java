import java.util.*;
public class digit {
    public static void main(String[] args) {
        Scanner sc = new Scanner(System.in);
        System.out.print("Enter a number: ");
        int num = sc.nextInt();
        int n = 0;
        while(num > 0){
            n++;
            num = num / 10; }
            System.out.println("Number of digits: " + n);

    } 
    ;
}
